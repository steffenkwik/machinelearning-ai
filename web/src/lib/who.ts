export type Sex = "laki-laki" | "perempuan";

export function whoStats(ageMonths: number, sex: Sex): [number, number] {
  const table = sex === "laki-laki" ? WHO_BOYS : WHO_GIRLS;
  const age = Math.min(Math.max(Math.round(ageMonths), 0), 60);
  return table[age];
}

/** Height-for-Age Z-score (HAZ). */
export function hazZScore(ageMonths: number, sex: Sex, heightCm: number): number {
  const [median, sd] = whoStats(ageMonths, sex);
  return (heightCm - median) / sd;
}
